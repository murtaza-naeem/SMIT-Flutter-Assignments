/*---Q.6: Write a program to check whether an alphabet is a vowel or consonant. ---*/
void main() {
  String Alphabate = "i";

  if (Alphabate == "a" ||
      Alphabate == "e" ||
      Alphabate == "i" ||
      Alphabate == "o" ||
      Alphabate == "u" ||
      Alphabate == "A" ||
      Alphabate == "E" ||
      Alphabate == "I" ||
      Alphabate == "O" ||
      Alphabate == "U") {
    print("$Alphabate is Vowel!!!");
  } else {
    print("$Alphabate is Consonant!!!");
  }
}
